// Helper function to get players by position
export const getPlayersByPosition = (position: NFLPlayer['position']): NFLPlayer[] => {
  return nflPlayers.filter(player => player.position === position);
};

// Helper function to search players
export const searchPlayers = (query: string): NFLPlayer[] => {
  const lowerQuery = query.toLowerCase();
  return nflPlayers.filter(player => 
    player.displayName.toLowerCase().includes(lowerQuery) ||
    player.nflTeam.toLowerCase().includes(lowerQuery) ||
    player.position.toLowerCase().includes(lowerQuery)
  );
};

// Helper function to get player by ID
export const getPlayerById = (id: string): NFLPlayer | undefined => {
  return nflPlayers.find(player => player.id === id);
};

